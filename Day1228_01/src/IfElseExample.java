
public class IfElseExample {
	public static void main(String[] args) {
		int score = 75;

		if (score >= 90) {
			System.out.println("A등급입니다!");
			System.out.println("소리벗고 팬티질러!");
		} else if (score >= 80) {
			System.out.println("B랑께");
			System.out.println("양호");
		} else if (score >= 70) {
			System.out.println("C요");
			System.out.println("C.Va");
		} else if (score >= 60) {
			System.out.println("D");
			System.out.println("이것도 너프해보시지");
		} else {
			System.out.println("FFFFFFFFF");
			System.out.println("자살");
		}
	}
}
